//OPERADORES MATEMÁTICOS

//Os operadores matemáticos são =(igual) +(Mais) -(Menos) *(Vezes) /(Dividir) %(Resto) ++(Incremento) --(Decremento)

//O =(igual) é utilizado na declaração da variável e para atribuir um novo valor
let nome = 'David'; //Declara a variável nome com o valor 'David'
nome = João; //Substitui o valor da variável por João

//O +(mais) é utilizado para somar e concatenar valores
//Exemplo de uso na concatenação
let c1 = 10;
let c2 = 23;
let concatenacao = c1 + c2; //Concatena os valores de c1 e c2 e atribui á variável resultado

//Exemplo de uso na soma de valores
let n1 = 10;
let n2 = 23;
let soma = parseInt(n1) + parseInt(n2); //Soma os valores convertidos de n1 e n2 e atribui á variável soma

//OBS: parseInt() converte o valor da variável dentro do parêntese para número inteiro
//Caso não converta, os valores são concatenados como no exemplo acima

//O -(menos) é utilizado para subtração de valores
//Exemplo de uso
let sub = n1 - n2; //Subtrai o valor da variável n1 em n2 (A partir daqui não precisa do uso do parseInt())

//O *(vezes) é utilizado para multiplicação de valores
//Exemplo de uso
let mult = n1 * n2; //Multiplica o valor da variável n1 em n2

//O /(divisão) é utilizado para dividir valores
//Exemplo de uso
let div = n1 / n2; //Divide o valor de n1 por n2

//O %(resto) resulta no resto da divisão dos valores
//Exemplo de uso
let resto = n1 % n2; //Resto da divisão de n1 por n2

//O ++(incremento) é utilizado para incrementar valores
//Exemplo de uso
n1++; //Nesse caso, a variável n1 recebe +1

//O --(decremento) é utilizado para decrementar valores
//Exemplo de uso
n1--; //Nesse caso a variável n2 perde -1