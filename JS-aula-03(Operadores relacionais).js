//OPERADORES RELACIONAIS
//Os operadores relacionais são utilizados para comparações em testes lógicos
//São eles ==(Igual), !=(Diferente), >(Maior), <(Menor), >=(Maior-igual), <=(Menor-igual)

let n1 = 20;
let n2 = 30;

console.log(n1 == n2); //Verifica se os valores de n1 e n2 são iguals, caso n1 for igual a n2, retorna true(verdadeiro), caso contrário, false(falso)

console.log(n1 != n2); //Verifica se n1 é diferente de n2 e tem o mesmo retorno acima (true ou false)

console.log(n1 > n2); //Verifica se n1 é maior que n2

console.log(n1 < n2); //Verifica se n1 é menor que n2

console.log(n1 >= n2); //Verifica se n1 é maior ou igual a n2

console.log(n1 <= n2); //Verifica se n1 é menor ou igual a n2